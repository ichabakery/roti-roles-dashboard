import { supabase } from '@/integrations/supabase/client';

export interface OrderItem {
  productId: string;
  productName: string;
  quantity: number;
  unitPrice: number;
}

export interface OrderFormData {
  customer_name: string;
  customer_phone: string;
  order_date: string;
  delivery_date: string;
  notes?: string;
  items: OrderItem[];
}

export interface Order {
  id?: string;
  order_number: string;
  branch_id: string;
  branch_name?: string;
  customer_name: string;
  customer_phone?: string;
  order_date: string;
  delivery_date: string;
  status: 'pending' | 'confirmed' | 'in_production' | 'ready' | 'completed' | 'cancelled';
  total_amount: number;
  notes?: string;
  items?: any;
  created_at?: string;
  created_by: string;
}

export const orderService = {
  async createOrder(orderData: OrderFormData & { branch_id: string, created_by: string }) {
    try {
      // Validate items
      if (!orderData.items || orderData.items.length === 0) {
        throw new Error('Pesanan harus memiliki minimal 1 item');
      }

      // Calculate total
      const total_amount = orderData.items.reduce((total, item) => 
        total + (item.quantity * item.unitPrice), 0);

      // Note: order_number will be auto-generated by database trigger
      const newOrder = {
        branch_id: orderData.branch_id,
        customer_name: orderData.customer_name,
        customer_phone: orderData.customer_phone,
        order_date: orderData.order_date,
        delivery_date: orderData.delivery_date,
        total_amount,
        items: orderData.items,
        notes: orderData.notes,
        status: 'pending' as const,
        created_by: orderData.created_by,
        order_number: '' // Will be overridden by trigger
      };

      const { data, error } = await supabase
        .from('orders')
        .insert(newOrder)
        .select()
        .single();

      if (error) throw error;
      return data;
    } catch (error) {
      console.error('Error creating order:', error);
      throw error;
    }
  },

  async getOrders(branch_id: string) {
    const { data, error } = await supabase
      .from('orders')
      .select('*')
      .eq('branch_id', branch_id)
      .order('created_at', { ascending: false });

    if (error) throw error;
    return data as Order[];
  },

  async updateOrderStatus(orderId: string, status: Order['status']) {
    const { data, error } = await supabase
      .from('orders')
      .update({ status })
      .eq('id', orderId)
      .select()
      .single();

    if (error) throw error;
    return data;
  }
};
